#include "so_long.h"

void	rev_anim(t_anim **anim, int rev)
{
	while ((*anim)->img.rev_print != rev)
	{
		(*anim)->img.rev_print = rev;
		(*anim) = (*anim)->next;
	}
	return ;
}
